def get_agent_price(agent_id: str) -> int:
    """–ü–æ–ª—É—á–∏—Ç—å —Ü–µ–Ω—É –∞–≥–µ–Ω—Ç–∞ –≤ –∫–æ–ø–µ–π–∫–∞—Ö"""
    agent = AGENT_PRICING.get(agent_id)
    return agent['price_monthly'] if agent else 0


def get_bundle_price(bundle_id: str) -> int:
    """–ü–æ–ª—É—á–∏—Ç—å —Ü–µ–Ω—É bundle –≤ –∫–æ–ø–µ–π–∫–∞—Ö"""
    bundle = AGENT_BUNDLES.get(bundle_id)
    return bundle['price_monthly'] if bundle else 0


def get_bundle_agents(bundle_id: str) -> List[str]:
    """–ü–æ–ª—É—á–∏—Ç—å —Å–ø–∏—Å–æ–∫ –∞–≥–µ–Ω—Ç–æ–≤ –≤ bundle"""
    bundle = AGENT_BUNDLES.get(bundle_id)
    if not bundle:
        return []
    
    if bundle['agents'] == 'all':
        return list(AGENT_PRICING.keys())
    
    return bundle['agents']


def calculate_regular_price(agent_ids: List[str]) -> int:
    """–†–∞—Å—Å—á–∏—Ç–∞—Ç—å –æ–±—ã—á–Ω—É—é —Ü–µ–Ω—É –¥–ª—è –Ω–∞–±–æ—Ä–∞ –∞–≥–µ–Ω—Ç–æ–≤"""
    total = 0
    for agent_id in agent_ids:
        total += get_agent_price(agent_id)
    return total


def recommend_bundle(agent_ids: List[str]) -> Dict[str, Any]:
    """
    –†–µ–∫–æ–º–µ–Ω–¥–æ–≤–∞—Ç—å bundle –µ—Å–ª–∏ –æ–Ω –≤—ã–≥–æ–¥–Ω–µ–µ
    
    Returns:
        Dict —Å –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–µ–π –æ —Ä–µ–∫–æ–º–µ–Ω–¥–∞—Ü–∏–∏ –∏–ª–∏ –ø—É—Å—Ç–æ–π dict
    """
    if len(agent_ids) < 2:
        return {}
    
    regular_price = calculate_regular_price(agent_ids)
    agent_set = set(agent_ids)
    
    # –ò—â–µ–º –ø–æ–¥—Ö–æ–¥—è—â–∏–µ bundles
    best_bundle = None
    best_savings = 0
    
    for bundle_id, bundle in AGENT_BUNDLES.items():
        bundle_agents = set(get_bundle_agents(bundle_id))
        
        # –ü—Ä–æ–≤–µ—Ä—è–µ–º —Å–æ–¥–µ—Ä–∂–∏—Ç –ª–∏ bundle –≤—Å–µ –Ω—É–∂–Ω—ã–µ –∞–≥–µ–Ω—Ç—ã
        if agent_set.issubset(bundle_agents):
            savings = regular_price - bundle['price_monthly']
            if savings > best_savings:
                best_savings = savings
                best_bundle = {
                    "bundle_id": bundle_id,
                    "bundle_name": bundle['name'],
                    "bundle_price": bundle['price_monthly'],
                    "regular_price": regular_price,
                    "savings": savings,
                    "savings_percent": round((savings / regular_price) * 100)
                }
    
    return best_bundle or {}

